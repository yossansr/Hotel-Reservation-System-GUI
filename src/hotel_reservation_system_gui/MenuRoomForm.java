    private void jTableRoomMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableRoomMouseClicked
        if(jComboBoxShowRoom.getSelectedItem().toString().equalsIgnoreCase("all room")) {
            DefaultTableModel tableModel = (DefaultTableModel) jTableRoom.getModel();
        
            int rIndex = jTableRoom.getSelectedRow();

            jTextFieldRoomNo.setText(tableModel.getValueAt(rIndex, 0).toString());
            jTextFieldType.setText(tableModel.getValueAt(rIndex, 1).toString());
            jTextFieldPrice.setText(tableModel.getValueAt(rIndex, 2).toString());
        }
    }//GEN-LAST:event_jTableRoomMouseClicked

