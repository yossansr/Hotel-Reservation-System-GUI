    private void jButtonAddNewRoomActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAddNewRoomActionPerformed
        String type = jTextFieldType.getText();
        
        if(jTextFieldRoomNo.getText().trim().equals("") || type.trim().equals("") || jTextFieldPrice.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Please fill The Blank Fields", "Empty Fields", JOptionPane.WARNING_MESSAGE);
        } else {
            int noRoom = Integer.valueOf(jTextFieldRoomNo.getText());
            float price = Float.valueOf(jTextFieldPrice.getText());
            room = new Room(noRoom, type, price);
            if(room.addRoom()) {
                JOptionPane.showMessageDialog(rootPane, "New Room Added Successfuly", "Add Room", JOptionPane.INFORMATION_MESSAGE);
                initTable();
                clearField();
            } else {
                JOptionPane.showMessageDialog(rootPane, "Room Not Added", "Add Room Failed", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButtonAddNewRoomActionPerformed

    private void jButtonRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRemoveActionPerformed
        DefaultTableModel tableModel = (DefaultTableModel) jTableRoom.getModel();
        int rIndex = jTableRoom.getSelectedRow();
        
        if(rIndex == -1) {
            JOptionPane.showMessageDialog(rootPane, "Click the data first", "Empty Fields", JOptionPane.WARNING_MESSAGE);
        } else {
            String type = jTextFieldType.getText();
            int noRoom = Integer.valueOf(jTextFieldRoomNo.getText());
            float price = Float.valueOf(jTextFieldPrice.getText());
            room = new Room(noRoom, type, price);
            if(room.deleteRoom()) {
                JOptionPane.showMessageDialog(rootPane, "Room Deleted", "Remove Room", JOptionPane.INFORMATION_MESSAGE);
                initTable();
                clearField();
            } else {
                JOptionPane.showMessageDialog(rootPane, "Room Failed to Delete", "Delete Room Failed", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButtonRemoveActionPerformed

