    private void initTable() {
        if(jComboBoxShowRoom.getSelectedItem().toString().equalsIgnoreCase("all room")) {
            jTableRoom.setModel(new DefaultTableModel(null, new Object[]{"No Room", "Type", "Price"}) {
            public boolean isCellEditable(int rowIndex, int columnIndex) {
                return false;
            }});
            room.showRoomInfo(jTableRoom);
        } else {
            clearField();
            jTableRoom.setModel(new DefaultTableModel(null, new Object[]{"No Room", "Day Start", "Day End"}) {
            public boolean isCellEditable(int rowIndex, int columnIndex) {
                return false;
            }});
            room.showBookedRoom(jTableRoom);
        }
        
    }
    
