    private void jButtonAddNewReceptionistActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAddNewReceptionistActionPerformed
        String name = jTextFieldName.getText();
        String phone = jTextFieldPhone.getText();
        String password = String.valueOf(jPasswordFieldPassword.getPassword());
        
        if(name.trim().equals("") || phone.trim().equals("") || password.trim().equals("") || jTextFieldSalary.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Please fill The Blank Fields", "Empty Fields", JOptionPane.WARNING_MESSAGE);
        } else {
            float salary = Float.valueOf(jTextFieldSalary.getText());
            receptionist = new Receptionist(name, phone, password, 0, salary);

            if(receptionist.addReceptionist()) {
                JOptionPane.showMessageDialog(rootPane, "New Receptionist Added Successfuly", "Add Receptionist", JOptionPane.INFORMATION_MESSAGE);
                initTable();
                clearFields();
            } else {
                JOptionPane.showMessageDialog(rootPane, "Receptionist Not Added", "Add Receptionist Failed", JOptionPane.ERROR_MESSAGE);
            }
            
        } 
        
        
    }//GEN-LAST:event_jButtonAddNewReceptionistActionPerformed

    private void jButtonShowSalaryActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonShowSalaryActionPerformed
        DefaultTableModel tableModel = (DefaultTableModel) jTableReceptionist.getModel();
        
        int rIndex = jTableReceptionist.getSelectedRow();
        
        if(rIndex == -1) {
            JOptionPane.showMessageDialog(rootPane, "Click the data first", "Empty Fields", JOptionPane.WARNING_MESSAGE);
        } else {
            SalaryInfoForm salaryInfo = new SalaryInfoForm(Integer.valueOf((tableModel.getValueAt(rIndex, 3).toString())), Float.valueOf(tableModel.getValueAt(rIndex, 4).toString()));
        
            salaryInfo.setVisible(true);
            salaryInfo.pack();
            salaryInfo.setLocationRelativeTo(null);
            salaryInfo.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
            clearFields();
        }
        
    }//GEN-LAST:event_jButtonShowSalaryActionPerformed

