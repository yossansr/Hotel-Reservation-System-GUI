    private void initTable() {
        jTableReceptionist.setModel(new DefaultTableModel(null, new Object[]{"id", "name", "phone", "totalOfReservating", "salary"}) {
            public boolean isCellEditable(int rowIndex, int columnIndex) {
                return false;
        }});
        MyConnection myConnection = new MyConnection();
        PreparedStatement ps;
        ResultSet rst;
        String sql = "SELECT * FROM receptionist";
        
        try {
            ps = myConnection.createConnection().prepareStatement(sql);
            rst = ps.executeQuery();
            DefaultTableModel tableModel = (DefaultTableModel) jTableReceptionist.getModel();
            Object[] row;
            while(rst.next()) {
                row = new Object[5];
                row[0] = rst.getInt(1);
                row[1] = rst.getString(2);
                row[2] = rst.getString(3);
                row[3] = rst.getInt(5);
                row[4] = rst.getFloat(6);
                
                tableModel.addRow(row);
            }
        } catch (SQLException ex) {
            Logger.getLogger(MenuReceptionistForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
