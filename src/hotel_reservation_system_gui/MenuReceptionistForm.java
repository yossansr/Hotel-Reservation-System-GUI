    private void jButtonRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRemoveActionPerformed
        DefaultTableModel tableModel = (DefaultTableModel) jTableReceptionist.getModel();
        int rIndex = jTableReceptionist.getSelectedRow();
        if(rIndex == -1) {
            JOptionPane.showMessageDialog(rootPane, "Click the data first", "Empty Fields", JOptionPane.WARNING_MESSAGE);
        } else {
            receptionist = new Receptionist((int) tableModel.getValueAt(rIndex, 0));
            if(receptionist.deleteReceptionist()) {
                JOptionPane.showMessageDialog(rootPane, "Delete Receptionist Successfuly", "Delete Receptionist", JOptionPane.INFORMATION_MESSAGE);
                initTable();
                clearFields();
            } else {
                JOptionPane.showMessageDialog(rootPane, "Receptionist Not Deleted", "Deleted Receptionist Failed", JOptionPane.ERROR_MESSAGE);
            }
            
        }
    }//GEN-LAST:event_jButtonRemoveActionPerformed

    private void jButtonEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditActionPerformed
        DefaultTableModel tableModel = (DefaultTableModel) jTableReceptionist.getModel();
        int rIndex = jTableReceptionist.getSelectedRow();
        String name = jTextFieldName.getText();
        String phone = jTextFieldPhone.getText();
        String password = String.valueOf(jPasswordFieldPassword.getPassword());
        
        if(rIndex == -1){
            JOptionPane.showMessageDialog(rootPane, "Click the data first", "Empty Fields", JOptionPane.WARNING_MESSAGE);
        } else {
            float salary = Float.valueOf(jTextFieldSalary.getText());
            receptionist = new Receptionist(name, phone, password, (int) tableModel.getValueAt(rIndex, 3), salary);

            if(receptionist.editReceptionist((int) tableModel.getValueAt(rIndex, 0))) {
                JOptionPane.showMessageDialog(rootPane, "Edit Receptionist Added Successfuly", "Edit Receptionist", JOptionPane.INFORMATION_MESSAGE);
                initTable();
                clearFields();
            } else {
                JOptionPane.showMessageDialog(rootPane, "Receptionist Not Edited", "Edit Receptionist Failed", JOptionPane.ERROR_MESSAGE);
            } 
        } 
    }//GEN-LAST:event_jButtonEditActionPerformed

